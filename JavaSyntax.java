// Una vez se compile el archivo ".java", el código que se ejecuta en Java está dentro de un archivo ".class".

// Una clase siempre debe comenzar con una primera letra mayúscula.
public class JavaSyntax {

    // El main() es un método obligatorio y lo tienen todo los programas en Java. Las llaves marcan el inicio y final de un código
    public static void main(String[]args) {

        // Podemos usar println() para imprimir una linea de texto en pantalla. Cada instrucción de código debe terminar con ";".
        System.out.println("Hola mundo!");

    }
    
}
